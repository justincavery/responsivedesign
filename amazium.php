<h1>Amazium</h1>
<figure class="resource-thumbnail"><img src="images/amazium.jpg" /><figcaption class="small">Amazium responsive Framework</figcaption></figure>
<p class="lead">This framework only came out of the woodwork after doing a lot of searching on the web.  It is not as popular as other frameworks like Foundation or Bootstrap, but is a neat little lightweight solution.</p>

<p>Strangely Mike describes the reasons behind the framework being that he wasn't able to find any 960 grid systems with 12 columns... something which I find to be the norm rather than the exception.</p><p>
More from Mike:</p>
<figure>
<blockquote>
<p>So what is Amazium I hear you ask, well you may have been hearing the term "Responsive Web Design" being thrown around the past few months, which simple means a website that can adjust to your screen size without having to make a separate website!  </p>
</blockquote>
<figcaption class="large quote">Mike, the developer of Amazium</figcaption>
</figure>

