<h1>What are Frameworks?</h1>
<p class="lead">Welcome to week six of the responsive design interview series, and a very Happy New Years to you all! Each week we ask our two guests the same four questions about Responsive Design and listen intently with what they have to say.</p>
<p>First of all lets take a closer look exactly what a responsive framework is. The definition of the term "framework" is...</p>

<ul>
  <li>A structure for supporting or enclosing something else, especially a skeletal support used as the basis for something being constructed.</li>

  <li>An external work platform; a scaffold.</li>

  <li> A fundamental structure, as for a written work.</li>

  <li>A set of assumptions, concepts, values, and practices that constitutes a way of viewing reality. </li>
</ul>

  <p>... and responsive frameworks are no different when it comes to websites.  They provide structure and tools that have been predefined to get you focussed on the layout and content of the site rather than the CSS and HTML (although these are <strong>very</strong> important also)</p>

  <p>The frameworks we have featured here have their place depending on the project you are working on.  If you're only looking for a quick responsive layout then you should try out <a href="http://alpha.responsivedesign.is/resources/frameworks/gridless">Gridless</a>, <a href="http://alpha.responsivedesign.is/resources/frameworks/simplegrid">SimpleGrid</a>, or  the <a href="http://alpha.responsivedesign.is/resources/frameworks/golden-grid-system">Gold Grid System</a>. On the other hand if you're looking for a prototyping solutions with buttons, modals, and sliders then you would look at something like <a href="http://alpha.responsivedesign.is/resources/frameworks/zurb-foundation-framework">Foundation</a>, <a href="http://alpha.responsivedesign.is/resources/frameworks/less-framework">Less Framework</a>, or <a href="http://alpha.responsivedesign.is/resources/frameworks/twitter-bootstrap">Twitter Bootstrap</a>.</p>

  <p>While Frameworks can give you a huge step up in getting started with your site, after all we used Zurb's Foundation to get the Alpha version of this site running, you should take some time to ask yourself <a href="http://alpha.responsivedesign.is/strategy/page-layout/should-i-use-a-responsive-framework-for-my-site">should I use a responsive framework for my site</a>.</p>		</article>
