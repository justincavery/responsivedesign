      <h1>Side Project of the Year - winner</h1>

<p class="lead">That is the message that I sent to my pregnant finance just after I picked up the award for Best Side Project of the Year at the .net magazine awards ceremony.</p>

<p>As I write this I'm on the train up to Leeds to spend the weekend with Laura and our friends which also gives me the perfect opportunity to try and express exactly how feel about the award.</p>

<p>First off there's two things I wanted to make clear up front:</p>
<ol>
<li>I nominated myself (that's embarrassing); and</li>
<li>I nominated myself twice! Once each for two of my projects (<a href="http://ami.responsivedesign.is">Am I Responsive</a> and <a href="http://responsivedesignweekly.com">Responsive Design Weekly</a>)</li>
</ol>

<p>I never expected to win the award. Partly because I had built <a href="http://ami.responsivedesign.is">Am I Responsive</a> to solve my own problems and I didn't think it was all that awesome, and mainly because for me there were two stand out winners in the <a href="">Pastry Box Project</a> and <a href="">Sidebar.io.</a></p>

<p>Both of these projects provide me so much joy.  The side bar gives me 5 great links on a daily basis that further my knowledge, while the pastry box project give me one new blog post every day from an amazing field of 30 authors each posting 12 times for the year.</p>

<p>Both of these projects are simply amazing and I highly recommend you subscribing to both of them.</p>

<h2>what was the actual project that won the award?</h2>

<p>The side project that won was something I built to achieve one thing.  Saving time.</p>

<p>I send an newsletter email about responsive design once a week.  The newsletter takes me about 4 hours each week to put together (give or take) including additional research, curating, writing introductions etc etc.</p>

<p>Each newsletter contains a feature site for the week and as part of that feature I include the site as the lead image in the newsletter showing off it's responsiveness in a desktop, mobile, tablet and mobile views.</p>

<p>Prior to "<a href="http://ami.responsivedesign.is">Am I Responsive</a>" I used to open the feature site in a browser and systematically take screen shots at each view port as I dragged the browser window from full screen to mobile.  Then I had to upload those to photoshop and position them within a layer containing the device image and position them as required before exporting to jpeg.</p>

<p>I suck at photoshop, and because of that it took me up to half an hour to do this each week.</p>

<p>After previously playing with <code>iframes</code> for some responsive testing back in 2010 I decided that I could shorten this process immensely with a similar approach.  Over the weekend I put together a series of <code>iframes</code> that sat nicely inside some desktop, laptop, tablet and phone device images.

<p>Voila.  <a href="http://ami.responsivedesign.is">Am I Responsive</a> was born.</p>

<h2>accepting an award you don't think you deserved</h2>

<p>Unfortunately my beautiful fiance wasn't able to make the award ceremony so I brought along the next best thing, my good mate <a href="">Al Cattell</a>.</p>

<p>Before the awards we both had shared a pint while he jokingly asked what I was going to say if I won.  I laughed it off and explained why I thought the other guys should definitely be in front of the tool I built.</p>

<p>At the table I spoke with Craig and Amy from Be Squared, an amazing initiative that produce brilliant conference videos that develops want to see.</p>

<p>As they showed the finalists and then the final finalists I was super chuffed to see that "<a href="http://ami.responsivedesign.is">Am I Responsive</a>" made the final three along with my favourite picks in <a href="http://sidebar.io">Sidebar.io</a> and the <a href="http://the-pastry-box-project.net/">Pastry Box</a>.</p>

<p>When Am I was read out I really couldn't believe it.</p>

<p>The thank you speech was a bit of a shambles. I know that I thanked the most important person which is Laura, but I feel a little bit bad because a few people said I gave Sascha a bit of grief (pardon the pun).  I have since followed up with Sascha on twitter and he assures me there was no harm done. I hope that's not really true because I honestly didn't mean that and hope everyone now goes and buys his book Learning Meteor now.</p>

<p>Even as I sat down I couldn't believe it and it wasn't until I chatted to another winner, <a href="http://laurakalbag.com/">Laura Kalbag</a> (otherwise known as New Comer of the Year), where she set my mind at rest.</p>

<p>She said that I should be looking solely on the tool itself, but also on the contribution to the community as a whole. When you take into consideration the time spent on the newsletter each week I began to realise where Laura was coming from.</p>

<p>On returning to the table I bumped into <a href="http://www.craiglockwood.co.uk/">Craig Lockwood</a> from <a href="http://www.besquare.me/">Be Squared</a>. As I went to explain again how I thought I was lucky he pointed out that he used the tool a lot for his next release, and the people sat next to him also used the tool often without even realising the person that put it together was sitting at the same table.  It turned out that most of the people I spoke with had at one point or another either heard of or used the tool itself.</p>

<h2>accepting gratitude</h2>

<p>For me this is the hardest thing to do.</p>

<p>When working for a company and publishing a client website after 3 months of hard work I would always send the thanks along to the implementors.</p>

<p>I think in everything that we do there are always people that you believe contribute more towards the achievable goal, it is very rare that you allow yourself to accept thanks directly.. often because you've built it as a team.</p>

<p>In this case the only other person was my finance Laura who put up with me being in a different country while we sorted out visas. I had no one else to pass thanks to except for every single person that found it a useful tool.</p>

<p><strong>THANK YOU</strong> for everyone that has used <a href="http://ami.responsivedesign.is">Am I Responsive</a> and who have also subscribed to <a href="http://responsivedesignweekly.com">RWD Weekly</a>. Each and every one of you make all of the late nights working on side projects so much more worthwhile.</p>

<p>At the end of the day I'm really proud to be able to say that something I built was able to help the wider community…and for that I thank you.</p>


<h2>No shortage of amazing people</h2>

<p>Alright, so aside from the super amazing and surprising experience of winning an award (I'm still coming to grips) the day was a brilliant opportunity to meet some great people.</p>

<p>It was super cool to meet <a href="">Neil</a> from <a href="http://mixture.io/">Mixture.io</a>, as well as <a href="">Mike Kus</a> from Front end whatever :)</p>

<p>It was awesome to meet the one and only solitary guy who runs the <a href="">Pastry Box Project</a>.  He's a French Literary major working in French Geneeva who is dabbling in the web.  Holy fuck! He's dabbling and doing that kind of stuff?!?! Seriously I was super blown away by that guy.</p>

<p>It was an honour to meet Mr <a href="">Robert Paul Lloyd</a>, the super duper tech guy out of Clear Left who writes a thousands words a minute for .net maagaine.  He was also kind enough to do an <a href="">interview for RWD Weekly</a> and rocked it like a….. well like a rock star.</p>

<p>I also met MR <a href="">Sascha Grief</a>.  He does some really amazing stuff online and produces one of the best daily emails I every seen.  If you think that weekly emails were great, this takes you to a new special place.  He also wrote the amazing <a href="">Getting Started with Meteor (which apparently I call "media" when I use my authentic Daaaarwin accent.).</p>

<p><a href="">Laura Kalbag</a> has contributed as an interviewee and we've had a few twitter based chats so it was great to finally put a real life personality to the online person.</p>

<p>For me the best thing this award has brought is the ratification and sure footed understanding that (the true belief that) what one person can build in relative isolation to the world can have a profound affect on the community at large.</p>

<p>This is the world that we live in today. The problems that you solve for yourself today will be enjoyed and built upon by the community as a whole tomorrow.</p>

<p>What an amazing industry we belong to, and why the hell would you ever want to be a part of anything else.</p>

