<h1>Navigation</h1><p class="lead">Navigation is key for your websites users to be able to find their way around your website. Navigation within responsive design is unique though because although you need to provide the same navigation options across multiple viewports you have a changing canvas size upon which to do so.</p>

<p>In this section we look at the specific requirements on <a href="http://alpha.responsivedesign.is/strategy/navigation/single-level">one level</a>, <a href="http://alpha.responsivedesign.is/strategy/navigation/two-level-navigation">two level</a>, and <a href="http://alpha.responsivedesign.is/strategy/navigation/multi-level-navigation">multiple level navigation</a> options and the kind of navigation that you will be working on depends on your current information architecture.</p>

<p>If you have a large and complex website that you are retro-fitting with a responsive design you will find the multi-level navigation helpful, however if you are going through a redesign/restructure of your entire website it is advisable to also visit re-visit your IA and because you might be able to make better use of the responsive design.</p>

<h2>When you say navigation do you mean...&nbsp;</h2>

<p>When we talk about navigation you should try hard not to just limit yourselves to the traditional top level and left hand navigation, but instead look at providing your users with the an easy way to navigate through your site to find the content and information they need.</p>

<p>This means looking past the traditional navigation mentioned above and begin to look at including contextual links within the content, provide lists of similar articles (and resources as we have done), providing breadcrumbs to easily jump within the section of the site, and even extending the search to provide contextual navigation</p>

